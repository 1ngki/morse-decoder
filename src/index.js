const MORSE_TABLE = {
  ".-": "a",
  "-...": "b",
  "-.-.": "c",
  "-..": "d",
  ".": "e",
  "..-.": "f",
  "--.": "g",
  "....": "h",
  "..": "i",
  ".---": "j",
  "-.-": "k",
  ".-..": "l",
  "--": "m",
  "-.": "n",
  "---": "o",
  ".--.": "p",
  "--.-": "q",
  ".-.": "r",
  "...": "s",
  "-": "t",
  "..-": "u",
  "...-": "v",
  ".--": "w",
  "-..-": "x",
  "-.--": "y",
  "--..": "z",
  ".----": "1",
  "..---": "2",
  "...--": "3",
  "....-": "4",
  ".....": "5",
  "-....": "6",
  "--...": "7",
  "---..": "8",
  "----.": "9",
  "-----": "0",
};

function decode(expr) {
  const mArray = expr.split().map((i) => i.match(/.{1,10}/g)).flat();
  let x = mArray.map((i) =>
    i.replace(/00/g, "").replace(/10/g, ".").replace(/11/g, "-")
  );
  let result = x.map((i) =>
    MORSE_TABLE[i] === undefined ? " " : MORSE_TABLE[i]
  );

  return result.join("");
}

module.exports = {
  decode,
};

